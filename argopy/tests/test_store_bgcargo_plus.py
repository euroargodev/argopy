class TestArgoFloatOpenDatasetBGCArgoPlus:
    """Test that ArgoFloat.open_dataset routes 'BGCArgoPlus' correctly."""

    def _make_mock_argofloat(self, wmo=TEST_WMO):
        """Return a minimal ArgoFloat-like mock that exposes the patched method."""
        from argopy.stores.float.spec import FloatStoreProto

        # Build a lightweight stub that only has the attributes open_dataset needs
        af = MagicMock()
        af.WMO = wmo
        af.cache = False
        af.cachedir = ""
        af.timeout = 0
        af._dataset = {}
        # Bind the real open_dataset from the patched spec
        af.open_dataset = FloatStoreProto.open_dataset.__get__(af, type(af))
        # ls_dataset should not be called for 'BGCArgoPlus'
        af.ls_dataset = MagicMock(side_effect=AssertionError("ls_dataset should not be called for BGCArgoPlus"))
        return af

    def test_bgcargo_plus_key_bypasses_gdac(self):
        """'BGCArgoPlus' must NOT call ls_dataset (GDAC lookup)."""
        dummy_ds = _make_dummy_dataset()
        af = self._make_mock_argofloat()

        with patch(
            "argopy.stores.float.spec.BGCArgoPlusStore",
            return_value=MagicMock(open_dataset=MagicMock(return_value=dummy_ds)),
        ):
            ds = af.open_dataset("BGCArgoPlus")
            assert isinstance(ds, xr.Dataset)
            af.ls_dataset.assert_not_called()

    def test_bgcargo_plus_stored_in_dataset_cache(self):
        """Result should be cached in self._dataset['BGCArgoPlus']."""
        dummy_ds = _make_dummy_dataset()
        af = self._make_mock_argofloat()

        with patch(
            "argopy.stores.float.spec.BGCArgoPlusStore",
            return_value=MagicMock(open_dataset=MagicMock(return_value=dummy_ds)),
        ):
            af.open_dataset("BGCArgoPlus")
            assert "BGCArgoPlus" in af._dataset

    def test_bgcplus_version_kwarg_forwarded(self):
        """version kwarg must reach BGCArgoPlusStore constructor."""
        dummy_ds = _make_dummy_dataset()
        af = self._make_mock_argofloat()
        custom_version = "v0.1_2025_12"
        mock_store_cls = MagicMock(
            return_value=MagicMock(open_dataset=MagicMock(return_value=dummy_ds))
        )

        with patch("argopy.stores.float.spec.BGCArgoPlusStore", mock_store_cls):
            af.open_dataset("BGCArgoPlus", version=custom_version)
            call_kwargs = mock_store_cls.call_args[1]
            assert call_kwargs.get("version") == custom_version

    def test_normal_dataset_unaffected(self):
        """Standard dataset names ('prof', 'Sprof', …) must still go through GDAC."""
        af = self._make_mock_argofloat()
        af.ls_dataset = MagicMock(return_value={"prof": "ftp://dummy/6903091_prof.nc"})

        # just verify ls_dataset is consulted — don't open the file
        datasets = af.ls_dataset()
        assert "prof" in datasets


