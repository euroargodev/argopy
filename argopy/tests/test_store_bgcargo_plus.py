TEST_WMO = 6903091
TEST_VERSION = "v0.1_2026_04"



def _make_dummy_dataset() -> xr.Dataset:
    """Return a minimal xarray.Dataset that looks like a BGC-Argo+ file."""
    n = 10
    return xr.Dataset(
        {
            "PRES": ("N_LEVELS", np.linspace(0, 2000, n)),
            "TEMP": ("N_LEVELS", np.random.uniform(2, 25, n)),
            "PSAL": ("N_LEVELS", np.random.uniform(33, 36, n)),
            "DOXY": ("N_LEVELS", np.random.uniform(150, 300, n)),
        },
        attrs={
            "WMO_INST_TYPE": str(TEST_WMO),
            "source": "BGC-Argo+",
            "version": TEST_VERSION,
        },
    )


